    function ADODB_Encrypt_MCrypt($cipher = null, $mode = null, $source = null) {
		if (!$cipher) {
			$cipher = MCRYPT_RIJNDAEL_256;
		}
		if (!$mode) {
			$mode = MCRYPT_MODE_ECB;
		}
		if (!$source) {
			$source = MCRYPT_RAND;
		}
		$this->_cipher = $cipher;
		$this->_mode = $mode;
		$this->_source = $source;
	}
